package com.pos.util;

public class CurrencySetting {

	int currencyId;
	String currSaleValue;
	String currReturnValue;
	public int getCurrencyId() {
		return currencyId;
	}
	public void setCurrencyId(int currencyId) {
		this.currencyId = currencyId;
	}
	public String getCurrSaleValue() {
		return currSaleValue;
	}
	public void setCurrSaleValue(String currSaleValue) {
		this.currSaleValue = currSaleValue;
	}
	public String getCurrReturnValue() {
		return currReturnValue;
	}
	public void setCurrReturnValue(String currReturnValue) {
		this.currReturnValue = currReturnValue;
	}
}
